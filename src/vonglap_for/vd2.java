package vonglap_for;

public class vd2 {
    public static void main(String[] args){
        for( int i=2;i<=20;i+=2) {
            System.out.println(i);
        }
    }
}
/*
Bài 2: In số chẵn
Viết chương trình in ra các số chẵn từ 2 đến 20.
✅ Gợi ý: Dùng vòng lặp for, kiểm tra số chẵn bằng điều kiện i % 2 == 0.
 */